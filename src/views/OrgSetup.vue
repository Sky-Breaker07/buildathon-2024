<template>
    <div class="min-h-screen bg-gray-100">
      <div class="container mx-auto px-4 py-8">
        <div class="flex justify-between items-center mb-6">
          <h2 class="text-3xl font-bold text-gray-900">Organization Setup</h2>
          <button @click="goToDashboard" class="bg-indigo-600 text-white px-4 py-2 rounded-md hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-indigo-500 focus:ring-offset-2">
            Back to Dashboard
          </button>
        </div>
        
        <div class="bg-white shadow-md rounded-lg p-6 mb-6">
          <h3 class="text-xl font-semibold mb-4">Add Professions</h3>
          <div class="flex items-center space-x-4 mb-4">
            <input
              v-model="newProfession"
              type="text"
              class="flex-grow px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500"
              placeholder="Enter profession name"
            >
            <button
              @click="addProfession"
              class="px-4 py-2 bg-indigo-600 text-white rounded-md hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-indigo-500 focus:ring-offset-2"
            >
              Add
            </button>
          </div>
          <div class="flex flex-wrap gap-2">
            <span
              v-for="profession in professions"
              :key="profession"
              class="px-3 py-1 bg-indigo-100 text-indigo-800 rounded-full text-sm flex items-center"
            >
              {{ profession }}
              <button
                @click="removeProfession(profession)"
                class="ml-2 text-indigo-600 hover:text-indigo-800 focus:outline-none"
              >
                <svg class="h-4 w-4" fill="none" stroke="currentColor" viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg">
                  <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6 18L18 6M6 6l12 12"></path>
                </svg>
              </button>
            </span>
          </div>
        </div>
  
        <div class="bg-white shadow-md rounded-lg p-6 mb-6">
          <h3 class="text-xl font-semibold mb-4">Register Head of Profession</h3>
          <form @submit.prevent="registerHeadOfProfession" class="space-y-4">
            <div class="grid grid-cols-2 gap-4">
              <input v-model="headForm.firstName" type="text" required class="px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500" placeholder="First Name">
              <input v-model="headForm.lastName" type="text" required class="px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500" placeholder="Last Name">
            </div>
            <input v-model="headForm.email" type="email" required class="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500" placeholder="Email">
            <select v-model="headForm.profession" required class="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500">
              <option value="">Select Profession</option>
              <option v-for="profession in professions" :key="profession" :value="profession">{{ profession }}</option>
            </select>
            <button type="submit" class="w-full px-4 py-2 bg-indigo-600 text-white rounded-md hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-indigo-500 focus:ring-offset-2">
              Register Head of Profession
            </button>
          </form>
        </div>
  
        <div class="bg-white shadow-md rounded-lg p-6 mb-6">
          <h3 class="text-xl font-semibold mb-4">Register Health Information Manager</h3>
          <form @submit.prevent="registerHealthInformationManager" class="space-y-4">
            <div class="grid grid-cols-2 gap-4">
              <input v-model="himForm.firstName" type="text" required class="px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500" placeholder="First Name">
              <input v-model="himForm.lastName" type="text" required class="px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500" placeholder="Last Name">
            </div>
            <input v-model="himForm.email" type="email" required class="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500" placeholder="Email">
            <button type="submit" class="w-full px-4 py-2 bg-indigo-600 text-white rounded-md hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-indigo-500 focus:ring-offset-2">
              Register Health Information Manager
            </button>
          </form>
        </div>
  
        <div class="bg-white shadow-md rounded-lg p-6">
          <h3 class="text-xl font-semibold mb-4">Registered Staff</h3>
          <div class="overflow-x-auto">
            <table class="min-w-full divide-y divide-gray-200">
              <thead class="bg-gray-50">
                <tr>
                  <th class="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Name</th>
                  <th class="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Staff ID</th>
                  <th class="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Email</th>
                  <th class="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Role</th>
                </tr>
              </thead>
              <tbody class="bg-white divide-y divide-gray-200">
                <tr v-for="staff in registeredStaff" :key="staff.staff_id">
                  <td class="px-6 py-4 whitespace-nowrap text-sm font-medium text-gray-900">{{ staff.name }}</td>
                  <td class="px-6 py-4 whitespace-nowrap text-sm text-gray-500">{{ staff.staff_id }}</td>
                  <td class="px-6 py-4 whitespace-nowrap text-sm text-gray-500">{{ staff.email }}</td>
                  <td class="px-6 py-4 whitespace-nowrap text-sm text-gray-500">{{ staff.role }}</td>
                </tr>
              </tbody>
            </table>
          </div>
        </div>
      </div>
    </div>
  </template>
  
  <script setup>
  import { ref, onMounted } from 'vue';
  import { useRouter } from 'vue-router';
  import { useStaffStore } from '../stores/staff-management';
  import { registerAdminHCP, registerHIM, getAdminHCPs, getHIMs } from '../utils/staffManagement';
  
  const router = useRouter();
  const staffStore = useStaffStore();
  
  const professions = ref([]);
  const newProfession = ref('');
  const headForm = ref({
    firstName: '',
    lastName: '',
    email: '',
    profession: '',
  });
  const himForm = ref({
    firstName: '',
    lastName: '',
    email: '',
  });
  const registeredStaff = ref([]);
  
  onMounted(async () => {
    await fetchRegisteredStaff();
  });
  
  const addProfession = () => {
    if (newProfession.value && !professions.value.includes(newProfession.value)) {
      professions.value.push(capitalizeFirstLetter(newProfession.value));
      newProfession.value = '';
    }
  };
  
  const removeProfession = (profession) => {
    professions.value = professions.value.filter(p => p !== profession);
  };
  
  const capitalizeFirstLetter = (string) => {
    return string.charAt(0).toUpperCase() + string.slice(1);
  };
  
  const registerHeadOfProfession = async () => {
    try {
      const response = await registerAdminHCP(headForm.value);
      staffStore.addAdminHealthcareProfessional(response.data.adminHealthcareProfessional);
      await fetchRegisteredStaff();
      resetHeadForm();
    } catch (error) {
      console.error('Error registering Head of Profession:', error);
    }
  };
  
  const registerHealthInformationManager = async () => {
    try {
      const response = await registerHIM(himForm.value);
      staffStore.addHealthInformationManager(response.data.healthInformationManager);
      await fetchRegisteredStaff();
      resetHIMForm();
    } catch (error) {
      console.error('Error registering Health Information Manager:', error);
    }
  };
  
  const resetHeadForm = () => {
    headForm.value = {
      firstName: '',
      lastName: '',
      email: '',
      profession: '',
    };
  };
  
  const resetHIMForm = () => {
    himForm.value = {
      firstName: '',
      lastName: '',
      email: '',
    };
  };
  
  const fetchRegisteredStaff = async () => {
    try {
      const [adminHCPs, hims] = await Promise.all([getAdminHCPs(), getHIMs()]);
      registeredStaff.value = [
        ...adminHCPs.data.adminHealthcareProfessionals.map(hcp => ({
          ...hcp,
          role: `Head of ${hcp.profession}`,
        })),
        ...hims.data.healthInformationManagers.map(him => ({
          ...him,
          role: 'Health Information Manager',
        })),
      ];
    } catch (error) {
      console.error('Error fetching registered staff:', error);
    }
  };
  
  const goToDashboard = () => {
    router.push('/dashboard');
  };
  </script>